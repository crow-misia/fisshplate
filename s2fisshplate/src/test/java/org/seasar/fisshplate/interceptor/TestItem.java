package org.seasar.fisshplate.interceptor;

import java.util.Date;

/**
 * @author rokugen
 */
public class TestItem {
	private int num;
	private String name;
	private Date date;
	
	public TestItem(String name, int num, Date date){
		this.name = name;
		this.num = num;
		this.date = date;	
	}
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}

}
